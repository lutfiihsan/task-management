<script>
    //get notification
    $(document).ready(function () {
        $.ajax({
            url: "{{ route('service.tasks.notification.show') }}",
            method: 'GET',
            headers: {
                'Authorization': 'Bearer ' + localStorage.getItem('token')
            },
            success: function (response) {
                console.log(response);
                if (response.task) {
                    updateNotificationUI(response.task);
                }
            },
            error: function (response) {
                console.log(response);
            }
        });

        function updateNotificationUI(task) {

            $('#notification-counter').text('1');

            const taskUrl = "{{ route('tugas.index') }}";

            const notificationItem = `
                <a class="dropdown-item d-flex align-items-center" href="#">
                    <div class="mr-3">
                        <div class="icon-circle bg-primary">
                            <i class="fas fa-file-alt text-white"></i>
                        </div>
                    </div>
                    <div>
                        <div class="small text-gray-500">${new Date(task.due_date).toLocaleDateString()}</div>
                        <span class="font-weight-bold" onclick="window.location.href = '${taskUrl}'">${task.title}</span>
                    </div>
                </a>
            `;

            $('#notification-list').append(notificationItem);
        }

        $('#logout-link').on('click', function (e) {
            e.preventDefault();

            $.ajax({
                url: "{{ route('service.logout') }}",
                method: 'POST',
                headers: {
                    'Authorization': 'Bearer ' + localStorage.getItem('token')
                },
                success: function (response) {
                    window.location.href = "{{ route('login') }}";
                },
                error: function (response) {
                    console.log(response);
                }
            });
        });
    });
</script>
